  // –°–æ–∑–¥–∞–µ–º –≤–∏–∑—É–∞–ª—å–Ω—ã–µ –∫–æ–Ω—Ü–µ–ø—Ü–∏–∏
  await createVisualConcepts(factoryDir);

  // –°–æ–∑–¥–∞–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç-–∫–∞–ª–µ–Ω–¥–∞—Ä—å
  await createContentCalendar(factoryDir);

  console.log(`üè≠ –ö–æ–Ω—Ç–µ–Ω—Ç-–∑–∞–≤–æ–¥ —Å–æ–∑–¥–∞–Ω: ${factoryDir}`);
  await closeDBConnection();
